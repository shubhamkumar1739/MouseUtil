package PointerUtils;

import DataUtils.QueueItem;
import PointerUtils.DataUtil.LogData;

import java.io.*;

public class LogUtil {
    public static void print(QueueItem item) {
        LogData data = (LogData) item.getmObject();
        File file = new File("/home/ironman/Documents/Coding/log.txt");
        try {
            FileWriter fw = new FileWriter(file, true);
            BufferedWriter bw = new BufferedWriter(fw);
            bw.write("\n" + data.logString);
            bw.close();
            fw.close();
        } catch (IOException e) {
            e.printStackTrace();
        }
    }
}
